set -e
set -x

if [ -z "$BASE64_KEY"]; then
  echo "ERROR: BASE64_KEY env variable required"
  exit 1
fi

if [ -z "$KEYFILE"]; then
  echo "ERROR: KEYFILE env variable required"
  exit 1
fi

echo $BASE64_KEY | base64 -d > $KEYFILE
